  let current = null;

  function detectDefault() {
    const nav = (navigator.language || 'en').slice(0, 2).toLowerCase();
    return MESSAGES[nav] ? nav : 'en';
  }

  function getLang() {
    if (!current) {
      let stored = null;
      try { stored = localStorage.getItem(STORAGE_KEY); } catch (_) { /* ignore */ }
      current = MESSAGES[stored] ? stored : detectDefault();
    }
    return current;
  }

  function setLang(code) {
    if (!MESSAGES[code]) return;
    current = code;
    try { localStorage.setItem(STORAGE_KEY, code); } catch (_) { /* ignore */ }
  }

  function t(key, params) {
    const lang = getLang();
    let s = (MESSAGES[lang] && MESSAGES[lang][key]) || MESSAGES.en[key] || key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        s = s.replaceAll(`{${k}}`, String(v));
      }
    }
    return s;
  }

  function isRTL() {
    return RTL.has(getLang());
  }

  return { LANGUAGES, MESSAGES, t, getLang, setLang, isRTL };
